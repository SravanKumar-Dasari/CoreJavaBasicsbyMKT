package javabasics;

public class C11_5_Methods_2_Global_Var {
	
	static int a = 20;
	static int b = 10;

	public static void main(String[] args) 
	{
		System.out.println("Addition Method");
		System.out.println("---------------");
		System.out.println(a+b);
		
		System.out.println();
		
		System.out.println("Subtraction Method");
		System.out.println("-------------------");
		System.out.println(a-b);
		
		System.out.println();
		
		System.out.println("Multiplication Method");
		System.out.println("---------------------");
		System.out.println(a*b);
		
		System.out.println();
		
		System.out.println("Division Method");
		System.out.println("---------------");
		System.out.println(a/b);
		
		System.out.println();
		
		System.out.println("Modulus Method");
		System.out.println("---------------");
		System.out.println(a%b);
		
	}

}
