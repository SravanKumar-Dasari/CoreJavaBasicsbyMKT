package javabasics;

public class C8_ForLoop_4_Ques_Assignment {

	public static void main(String[] args) 
	{
	
	System.out.println("*** write a programme to print from -5 to -15 using for-loop ? ***");	
	
	for (int i=-5; i>=-15; i--)
	{
		System.out.println(i);
	}
		
	System.out.println("---------------------------------------------------");
	
	System.out.println("*** write a programme to print only Even numbers from 1 to 50 using for-loop ? ***");	
	
	for (int i=2; i<=50; i++)
	{
		System.out.println(i);
		i++;
	}
		
	System.out.println("---------------------------------------------------");
	
	System.out.println("*** write a programme to print only Odd numbers from 1 to 50 using for-loop ? ***");	
	
	for (int i=1; i<=50; i++)
	{
		System.out.println(i);
		i++;
	}
		
	
	}

}
