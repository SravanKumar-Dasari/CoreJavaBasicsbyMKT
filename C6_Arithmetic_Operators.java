package javabasics;

public class C6_Arithmetic_Operators {
	
	static void addition()
	{
		int a = 100;
		int b = 50;
		int d = a+b;
		System.out.println(d);
	}
	
	static void subtraction()
	{
		int a = 100;
		int b = 90;
		int c = a-b;
		System.out.println(c);
	}
	
	static void multiplication()
	{
		int a = 100;
		int b = 90;
		int c = a*b;
		System.out.println(c);
	}
	
	static void division()
	{
		int a = 50;
		int b = 25;
		int c = a/b;
		System.out.println(c);
	}
	
	static void modulus()
	{
		int a = 50;
		int b = 25;
		int c = a%b;
		System.out.println(c);
	}
	

	public static void main(String[] args) {
		// TODO Auto-generated method stub
	
	addition();
	subtraction();
	multiplication();
	division();
	modulus();

	}

}
