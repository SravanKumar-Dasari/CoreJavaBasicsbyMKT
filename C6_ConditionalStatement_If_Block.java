package javabasics;

public class C6_ConditionalStatement_If_Block {

	public static void main(String[] args) 
	{
		int i = 10;
		int j = 20;
		
		// if i value is above the j value then print the console
		if (i>j)
		{
			System.out.println("1st");
		}
		
		// if i value is below the j value then print the console 
		if (i<j)
		{
			System.out.println("2nd");
		}
		
		// if i value is greater-than or equals to the j value then print the console
		if (i>=j)
		{
			System.out.println("3rd");
		}
		
		// if i value is lesser-than or equals to the j value then print the console
		if (i<=j)
		{
			System.out.println("4th");
		}
		
		// if i value is equals-to-equals-to to the j value then print the console
		if (i==j)
		{
			System.out.println("5th");
		}
		
		//if i value is  not equals-to to the j value then print the console
		if (i!=j)
		{
			System.out.println("6th");
		}
		
	}

}
