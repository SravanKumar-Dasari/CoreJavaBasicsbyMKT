package javabasics;

public class C8_ForLoop_2_Ques {

	public static void main(String[] args) 
	{
	
	System.out.println("*** write a program to print from 1 to 10 by using for-loop ? ***"); 
		
	for (int i=1; i<=10; i++)
	{
		System.out.println(i);
	}
		
	System.out.println("-----------------------------------");
	
	System.out.println("*** write a program to print from 17 to 34 by using for-loop ?");
	
	for (int j=17; j<=34; j++)
	{
		System.out.println(j);
	}

	System.out.println("-----------------------------------");
	
	System.out.println("*** write a program to print from -10 to 10 by using for-loop ?");
	
	for (int k=-10; k<=10; k++)
	{
		System.out.println(k);
	}
	
	}

}
