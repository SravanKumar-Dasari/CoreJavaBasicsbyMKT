package javabasics;

public class C18_Access_Specifier_for_Method_Inside_Package1 
{
	
	private void add()
	{
		System.out.println("Addition");
	}
	
	public void sub()
	{
		System.out.println("Subtraction");
	}
	
	protected void multi() 
	{
		System.out.println("Multiple by");
	}
	
	void div()
	{
		System.out.println("Divided by");
	}
	
}
